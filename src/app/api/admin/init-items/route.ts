export async function POST(req: Request) {
  const authUser = await verifyToken(req);
  if (!authUser) return NextResponse.json({ error: 'Unauthorized' }, { status: 401 });

  const groupId = await getActualGroupId(authUser.uid);
  if (!isSuperOrMaster(groupId, authUser.email)) {
    return NextResponse.json({ error: 'Forbidden — master/superuser only' }, { status: 403 });
  }

  const { storeId, mode = 'skip' } = await req.json();
  if (!storeId) return NextResponse.json({ error: 'storeId 필수' }, { status: 400 });

  const col = adminDb.collection('items');
  const now = FieldValue.serverTimestamp();
  let saved = 0;
  let skipped = 0;

  /* mode=reset → 기존 전체 삭제 후 재등록 */
  if (mode === 'reset') {
    const existing = await col.where('storeId', '==', storeId).get();
    const delBatch = adminDb.batch();
    existing.docs.forEach(d => delBatch.delete(d.ref));
    if (!existing.empty) await delBatch.commit();
  }

  /* 배치 쓰기 (500개 제한 → 청크) */
  const CHUNK = 400;
  for (let i = 0; i < SEED_ITEMS.length; i += CHUNK) {
    const chunk = SEED_ITEMS.slice(i, i + CHUNK);
    const batch = adminDb.batch();
    for (const item of chunk) {
      if (mode === 'skip') {
        /* 이미 같은 category+cut+grade 가 있으면 스킵 */
        const dup = await col
          .where('storeId', '==', storeId)
          .where('category', '==', item.category)
          .where('cut', '==', item.cut)
          .where('grade', '==', item.grade)
          .limit(1).get();
        if (!dup.empty) { skipped++; continue; }
      }
      const prices = calcItemPrices(item.buyPrice, item.targetMargin, item.appliedCost, item.lossRate);
      const ref = col.doc();
      batch.set(ref, { ...item, ...prices, storeId, priceHistory: [], createdAt: now, updatedAt: now });
      saved++;
    }
    await batch.commit();
  }

  return NextResponse.json({ success: true, saved, skipped, total: SEED_ITEMS.length });
}
